package FirstPackage;

public class Switch {

	public static void main(String[] args) {
		
		String name ="Achala";
		
		switch(name)
		{
		case "Qasim":
			System.out.println("I will talk to Qasim");
			break;
			
		case "Usman":
			System.out.println("I will talk to Usman");
			break;
			
		case "Achala":
			System.out.println("I will talk to Achala");
			break;
			
		case "Suhaib":
			System.out.println("I will talk to Suhaib");
			break;
			
		case "Ashwini":
			System.out.println("I will talk to Ashwini");
			break;
		
		default:
			System.out.println("Sleep");

		}
	}

}
